import { execFile } from 'node:child_process';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { createRequire } from 'node:module';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { promisify } from 'node:util';
import { platformTargets } from './platform-targets.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..');
const outputPath = resolve(repoRoot, 'src/config/generated/browser-profiles.ts');
const execFileAsync = promisify(execFile);
const require = createRequire(import.meta.url);

function isMuslRuntime() {
  if (process.platform !== 'linux') {
    return false;
  }

  if (typeof process.report?.getReport === 'function') {
    const report = process.report.getReport();

    if (report?.header?.glibcVersionRuntime) {
      return false;
    }
  }

  return true;
}

function resolveLocalBinary() {
  const target = platformTargets.find(({ os, cpu, libc }) => {
    if (!os.includes(process.platform) || !cpu.includes(process.arch)) {
      return false;
    }

    if (!libc) {
      return true;
    }

    return libc.includes(isMuslRuntime() ? 'musl' : 'glibc');
  });

  return target ? resolve(repoRoot, 'rust', target.binaryName) : null;
}

async function loadProfiles() {
  const localBinary = resolveLocalBinary();

  if (!localBinary) {
    throw new Error(
      `Unsupported host platform for profile generation: ${process.platform}-${process.arch}`
    );
  }

  const binding = require(localBinary);

  return binding.getProfiles();
}

try {
  const profiles = await loadProfiles();
  const content = `// This file is auto-generated by scripts/generate-browser-profiles.mjs\n// Do not edit it manually.\n\nexport const BROWSER_PROFILES = ${JSON.stringify(
    profiles,
    null,
    2
  )} as const;\n\nexport type BrowserProfile = (typeof BROWSER_PROFILES)[number];\n`;

  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, content, 'utf8');
  await execFileAsync(resolve(repoRoot, 'node_modules/.bin/oxfmt'), ['--write', outputPath], {
    cwd: repoRoot,
  });
} catch (error) {
  try {
    await readFile(outputPath, 'utf8');
  } catch {
    throw error;
  }
}
